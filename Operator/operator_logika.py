#operator perbandingan / relasi merupakan operator membandingkan dua buah nilai
# dengan output value atau nilai boolean (T/F)
print("============= Operasi Logika =================")

x = True
y = False

print("Diketahui Nilai x : ", x)
print("Diketahui Nilai y : ", y)

print("============= Operasi Relasi AND =================")
print("x And y  Hasilnya",(x and y))
print("x And x  Hasilnya",(x and x))
print("y And x  Hasilnya",(y and x))
print("y And y  Hasilnya",(y and y))

print("============= Operasi Relasi OR =================")
print("x Or y  Hasilnya",(x or y))
print("x Or x  Hasilnya",(x or x))
print("y Or x  Hasilnya",(y or x))
print("y Or y  Hasilnya",(y or y))

print("============= Operasi Relasi NOT =================")
print("Not x  Hasilnya",(not x))
print("Not y  Hasilnya",(not y))
